#include "libft.h"

int	ft_isspace(const char *str)
{
	if (*str == ' '
		|| *str == '\t'
		|| *str == '\r'
		|| *str == '\f'
		|| *str == '\v'
		|| *str == '\n')
		return (1);
	else
		return (0);
}